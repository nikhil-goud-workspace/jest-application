export function add(a: number, b: number) {
    return a + b;
}
export function getObject() {
    return { a: 1, b: 2 };
}
export function isPositive(n: number) {
    return n > 0 ? 'yes' : null;
}
export function multiply(a: number, b: number) {
    return a * b;
}
export function approxPi() {
    return 3.14159;
}
